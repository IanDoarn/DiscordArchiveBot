def ping_author(message: Message, *args):
    return f"{message.author.mention}"


def get_channels(message: Message, *args):
    msg = f"Channels in {str(message.guild)}\n"
    for x in message.guild.channels:
        msg += f"{x.name}\n"
    return msg
